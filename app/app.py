def create_plot():
    """Create a scatterplot of the iris dataset using plotnine."""
    # Load the data
    iris_df = load_iris_data()
    
    # Create the plot
    plot = (
        ggplot(iris_df, aes(x='sepal_length', y='sepal_width', color='species')) +
        geom_point(size=3, alpha=0.7) +
        labs(
            title='Iris Dataset Scatterplot',
            x='Sepal Length (cm)',
            y='Sepal Width (cm)',
            color='Species'
        ) +
        theme_minimal() +
        scale_color_brewer(type='qual', palette='Set1')
    )
    
    # Save the plot to a BytesIO object
    img_buffer = BytesIO()
    plot.save(img_buffer, format='png', dpi=100)
    img_buffer.seek(0)
    
    # Encode the image to base64 for embedding in HTML
    img_str = base64.b64encode(img_buffer.read()).decode('utf-8')
    
    return img_str

@app.route('/')
def index():
    """Render the main page with the iris dataset scatterplot."""
    plot_img = create_plot()
    return render_template('index.html', plot_img=plot_img)

